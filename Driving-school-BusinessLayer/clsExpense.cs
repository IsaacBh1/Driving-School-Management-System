using DrivingSchool_DataAccessLayer;
using System.Data;
using System.Xml.Linq;

namespace Driving_school_BusinessLayer
{
    public class clsExpense
    {
        enum enMode { AddNew = 0, Update = 1 }
        enMode Mode = enMode.AddNew;

        public int ExpenseID { get; set; }
        public int ExpenseTypeID { get; set; }
        public decimal Amount { get; set; }
        public string AdditionalNotes { get; set; }
        public int MoneyBankID { get; set; }

        public clsExpense()
        {
            ExpenseID = -1;
            ExpenseTypeID = -1;
            Amount = 0;
            AdditionalNotes = string.Empty;
            MoneyBankID = -1;
            Mode = enMode.AddNew;
        }

        private clsExpense(int expenseID, int expenseTypeID, decimal amount, string additionalNotes, int moneyBankID)
        {
            ExpenseID = expenseID;
            ExpenseTypeID = expenseTypeID;
            Amount = amount;
            AdditionalNotes = additionalNotes;
            MoneyBankID = moneyBankID;
            Mode = enMode.Update;
        }

        private bool _AddNewExpense()
        {
            ExpenseID = clsExpenseDataAccess.AddNewExpense(ExpenseTypeID, Amount, AdditionalNotes, MoneyBankID);
            return ExpenseID != -1;
        }

        private bool _UpdateExpense()
        {
            return clsExpenseDataAccess.UpdateExpense(ExpenseID, ExpenseTypeID, Amount, AdditionalNotes, MoneyBankID);
        }

        public bool Save()
        {
            switch (Mode)
            {
                case enMode.AddNew:
                    if (_AddNewExpense())
                    {
                        Mode = enMode.Update;
                        return true;
                    }
                    return false;
                case enMode.Update:
                    return _UpdateExpense();
            }
            return false;
        }

        public static clsExpense Find(int ID)
        {
            int _ExpenseTypeID = -1;
            decimal _Amount = 0;
            string _AdditionalNotes = string.Empty;
            int _MoneyBankID = -1;

            if (clsExpenseDataAccess.GetExpenseInfoByID(ID, ref _ExpenseTypeID, ref _Amount, ref _AdditionalNotes, ref _MoneyBankID))
            {
                return new clsExpense(ID, _ExpenseTypeID, _Amount, _AdditionalNotes, _MoneyBankID);
            }
            return null;
        }

        public static DataTable GetAllExpencesTypes()
        {
            return clsExpenceTypeDataAccess.GetAllExpenceTypes();
        }


        public static bool AddNewExpenceType(string Name, string Description)
        {
            return clsExpenceTypeDataAccess.AddNewExpenceType(Name, Description) != -1; 
        }

        public static int GetExpenseTypeIDByName(string Name)
        {
            return clsExpenceTypeDataAccess.GetExpenceTypeIDByName(Name); 
        }


        public static string GetFuelTypeNameByID(int ID)
        {
            return clsExpenceTypeDataAccess.GetExpenceTypeNameByID(ID) ; 
        }

        public static DataTable GetAllExpences()
            => clsExpenseDataAccess.GetAllExpenses();


        public static bool DeleteExpenceType(int Id)
            => clsExpenceTypeDataAccess.DeleteExpenceType(Id);


        public static DataTable GetAllExpensesStatistics()
        {
            return clsExpenseDataAccess.GetAllExpensesStatistics();
        }

    }
}
